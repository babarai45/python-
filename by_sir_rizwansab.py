 # int data type (int)
# int is a whole number, positive or negative,  without decimals, of unlimited length.
# # it default value is 0
# example:
x=int()  # it default value is 0
print(x) # 0
a,*b=10,20,40,50
print(a) # 10
print(b) # [20, 40, 50] # *b is a list

# float data type (float)
# float is a number, positive or negative, containing one or more decimals.
# float data type can be represented in three ways
# it default value is 0.0
# example:
x=float()  # it default value is 0.0
print(x) # 0.0
a,*b=10.5,20.5,40.5,50.5
print(a) # 10.5
print(b) # [20.5, 40.5, 50.5] # *b is a list


